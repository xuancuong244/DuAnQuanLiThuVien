    private void selectImage() {
        try {
            JFileChooser f = new JFileChooser("src\\com\\poly\\image");
            f.showOpenDialog(this);

            File file = f.getSelectedFile();
            if (XShare.saveLogo(file)) {
                // Hiển thị hình lên form
                lblHinh1.setIcon(ResizeImage(String.valueOf(XShare.readLogo(file.getName()))));
                lblHinh1.setToolTipText(file.getName());
            }
        } catch (HeadlessException ex) {
            System.out.println("No photo selected");
        }
    }

    private ImageIcon ResizeImage(String ImagePath) {
//        System.out.println(ImagePath);
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(lblHinh1.getWidth(), lblHinh1.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }

