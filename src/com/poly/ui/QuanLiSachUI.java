    private void load() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSach.getModel();
        model.setRowCount(0);
        try {
            list.clear();
            String keyword = txtFind.getText();
            if (!keyword.isEmpty()) {
                list = dao.selectByKeyword(keyword);
            } else {
                list = dao.select();
            }
            for (QuanLiSach s : list) {
                Object[] row = {
                    s.getMaSach(),
                    s.getTenSach(),
                    s.getLoai(),
                    s.getTacGia(),
                    s.getNXB(),
                    s.getNgayNhap(),
                    s.getSoLuong(),
                    s.getNoiDung(),
                    s.getHinh(),
                    s.getUrl()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
//            XDialog.alert(this, "Error data query!");
//            System.out.println(e);
            e.printStackTrace();
        }

    }

