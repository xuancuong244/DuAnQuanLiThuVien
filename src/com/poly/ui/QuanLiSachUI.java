    private boolean isValidate(QuanLiSach s) {
        if (s.getMaSach().equals("")) {
            XDialog.alert(this, "Id Book can't be empty");
            txtID.requestFocus();
            return false;
        }
        if (s.getTenSach().equals("")) {
            XDialog.alert(this, "Name Book can't be empty");
            txtName.requestFocus();
            return false;
        }
        if (s.getNXB().equals("")) {
            XDialog.alert(this, "Publisher can't be empty");
            cboPub.requestFocus();
            return false;
        }
        if (s.getLoai().equals("")) {
            XDialog.alert(this, "Category can't be empty");
            cboCategory.requestFocus();
            return false;
        }
        if (s.getTacGia().equals("")) {
            XDialog.alert(this, "Author can't be empty");
            cboAuthor1.requestFocus();
            return false;
        }

        String sl = txtAmount.getText();
        try {
            int soLuong = Integer.parseInt(sl);
            // SoLuong > 0
            if (soLuong <= 0) {
                XDialog.alert(this, "Amount must be greater than 0");
                txtAmount.requestFocus();
                return false;
            }
        } catch (Exception e) {
            XDialog.alert(this, "Amount must be number");
            txtAmount.requestFocus();
            return false;
        }

        if (s.getNgayNhap().equals("")) {
            XDialog.alert(this, "Date can't be empty");
            jDateChooser.requestFocus();
            return false;
        }
        if (s.getNoiDung().equals("")) {
            XDialog.alert(this, "Contents can't be empty");
            txtContent.requestFocus();
            return false;
        }
        if (s.getHinh().equals("")) {
            XDialog.alert(this, "Image can't be empty");
            lblHinh1.requestFocus();
            return false;
        }
        if (s.getUrl().equals("")) {
            XDialog.alert(this, "Url can't be empty");
            txtUrl.requestFocus();
            return false;
        }
        return true;
    }

