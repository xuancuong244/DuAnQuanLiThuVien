        private boolean isValidate(NhaXuatBan s) {
        if (s.getMaNXB().equals("")) {
            XDialog.alert(this, "Id can't be empty");
            txtID.requestFocus();
            return false;
        }
        if (s.getTenNXB().equals("")) {
            XDialog.alert(this, "Name can't be empty");
            txtName.requestFocus();
            return false;
        }

        if (s.getDiaChi().equals("")) {
            XDialog.alert(this, "Address can't be empty");
            txtAddress.requestFocus();
            return false;
        }

// check email
        String email = txtEmail.getText();
        if (email.equals("")) {
            XDialog.alert(this, "You have not entered your Email");
            txtEmail.requestFocus();
            return false;
        }
        try {
            TestEmail.parseEmail(email);
        } catch (Exception e) {
            XDialog.alert(this, "Email is not correct");
//            e.printStackTrace();
            txtEmail.requestFocus();
            return false;
        }
        if (s.getSDT().equals("")) {
            XDialog.alert(this, "Phone can't be empty");
            txtPhone.requestFocus();
            return false;
        }

        return true;
    }
