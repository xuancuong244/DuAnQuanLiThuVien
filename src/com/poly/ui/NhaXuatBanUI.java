    private void load() {
        DefaultTableModel tblmodel = (DefaultTableModel) tblDanhSach.getModel();
        tblmodel.setRowCount(0);
        try {
            String keyword = txtSearch.getText();
            if (!keyword.isEmpty()) {
                list = dao.selectByKeyword(keyword);
            } else {
                list = dao.select();

            }
            for (NhaXuatBan dg : list) {
                Object[] row = {
                    dg.getMaNXB(),
                    dg.getTenNXB(),
                    dg.getDiaChi(),
                    dg.getEmail(),
                    dg.getSDT()
                };
                tblmodel.addRow(row);
            }
        } catch (Exception e) {
            XDialog.alert(this, "Error data query");
        }
    }

    /**
     * Cap nhat thong tin nhan vien
     */
    private void edit() {
        try {
            String maNXB = (String) tblDanhSach.getValueAt(this.index, 0);
            NhaXuatBan model = dao.selectById(maNXB);
            if (model != null) {
                this.setModel(model);
                this.setStatus(false);
            }
        } catch (Exception e) {
            XDialog.alert(this, "Error data query!");
        }
    }

    void insert() {
        NhaXuatBan model = getModel();
        if(!isValidate(model)){
            return;
        }
        try {
            dao.insert(model);
            this.load();
            this.clear();
            XDialog.alert(this, "Add Publisher successful!");
        } catch (Exception e) {
            XDialog.alert(this, "Add Publisher fail!");
        }
    }

    void update() {
        NhaXuatBan model = getModel();
        try {
            dao.update(model);
            this.load();
            XDialog.alert(this, "Update Publisher successful!");
        } catch (Exception e) {
            XDialog.alert(this, "Update Publisher fail!");
        }
    }

    void delete() {
         if (!XShare.isManager()) {
            XDialog.alert(this, "You do not have permission to delete Publisher!");
        } else {
        if (XDialog.confirm(this, "Are you sure to delete this Publisher?")) {
            String matg = txtID.getText();
            try {
                dao.delete(matg);
                this.load();
                this.clear();
                XDialog.alert(this, "Delete Publisher successful!");
            } catch (Exception e) {
                XDialog.alert(this, "Delete Publisher fail!");
            }
        }
         }
    }

